  Chess pieces move uniquely: Pawns move forward one square (two on first move, diagonal captures), Rooks move horizontally/vertically, Bishops move diagonally, Knights jump in an 'L', Queens move in any direction, and Kings move one square in any direction. Key special moves include castling (0-0/0-0-0), en passant, and pawn promotion.

  Individual Piece Movements
  - King: Moves one square in any direction.
  - Queen: Moves any number of vacant squares diagonally, horizontally, or vertically.
  - Rook: Moves any number of vacant squares horizontally or vertically.
  - Bishop: Moves any number of vacant squares diagonally.
  - Knight: Moves in an 'L-shape': two squares in a straight direction, then one square perpendicular. It is the only piece that can jump over others.
  - Pawn: Moves one square forward (or two on its first move), and captures diagonally one square forward.

  Special Moves
  - Castling: A move involving the king and a rook, used to protect the king and activate the rook.
  - En Passant: A special pawn capture that occurs when a pawn moves two squares forward and lands beside an opponent's pawn.
  - Pawn Promotion: When a pawn reaches the opposite end of the board, it can be promoted to a queen, rook, bishop, or knight.

  Common Opening Moves
  1. e4 (King's Pawn Game): Often leads to open, attacking games like the Ruy Lopez or Italian Game.
  2. d4 (Queen's Pawn Game): Usually results in more strategic, closed games such as the Queen's Gambit.
  3. c4 (English Opening): A flexible opening that often transposes into other systems.

  Key Chess Terminology
  - Check (+): The king is directly attacked by an opponent's piece.
  - Checkmate (#): The king is under attack and cannot escape, winning the game.
  - Stalemate: The player whose turn it is has no legal moves but is not in check, resulting in a draw.
  - Capture: A piece replaces an opponent's piece on its square to remove it from the board.
